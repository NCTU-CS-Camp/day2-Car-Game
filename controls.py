"""
--------------------------------------------------------
#### 這個檔案負責「把按鍵變成車子的動作」(Q2、Q3)
--------------------------------------------------------
"""

import pygame

from car import ACCEL, ROTATE_SPEED


def handle_movement(keys, car):
    """
    --------------------------------------------------------
    #### 功能 : 根據目前按住的鍵，控制車子前進/後退/轉向
    --------------------------------------------------------
    #### 參數
    - keys : pygame.key.get_pressed() 的結果，代表「現在鍵盤的狀態」。
             想知道某個鍵有沒有被按住，就用 keys[鍵的名字]，
             例如 keys[pygame.K_w]：
                - W 有被按住 → 得到 True
                - W 沒被按住 → 得到 False
             所以可以直接把它放進 if，例如：
                if keys[pygame.K_w]:   # 意思是「如果 W 被按住」
    - car  : 車子物件，用它的方法(set_accel、rotate)讓車子動起來
    --------------------------------------------------------
    #### 回傳值
    - 無(直接呼叫 car 的方法讓車子動)
    --------------------------------------------------------
    """
    """
    --------------------------------------------------------
    Q2. 完成 W、S 前進/後退的判斷
    --------------------------------------------------------
    【怎麼判斷按鍵】(同上方參數說明)
        keys[pygame.K_w] → W 有沒有被按住 (True / False)
        keys[pygame.K_s] → S 有沒有被按住 (True / False)

    【要做的事】
        1. 按住 W       → car.set_accel(ACCEL)    車子前進
        2. 否則按住 S   → car.set_accel(-ACCEL)   車子後退
        3. 兩個都沒按   → car.set_accel(0)        停止加速(不再變快)

    【小提醒】
        這三種情況「同時只會發生一種」，
        所以用 if / elif / else 串在一起就好。
    --------------------------------------------------------
    """
    # Q2 begin
    if keys[pygame.K_w]:
        car.set_accel(ACCEL)
    elif keys[pygame.K_s]:
        car.set_accel(-ACCEL)
    else:
        car.set_accel(0)
    # Q2 end

    """
    --------------------------------------------------------
    Q3. 完成 J、K 轉向的判斷
    --------------------------------------------------------
    【怎麼判斷按鍵】(方法跟 Q2 一樣)
        keys[pygame.K_j] → J 有沒有被按住 (True / False)
        keys[pygame.K_k] → K 有沒有被按住 (True / False)

    【要做的事】
        - 按住 J → car.rotate(-ROTATE_SPEED)   車子左轉
        - 按住 K → car.rotate(ROTATE_SPEED)    車子右轉

    【小提醒】
        轉向跟前進/後退不一樣：J、K 各自獨立、互不影響，
        所以要用「兩個分開的 if」，不要用 elif。
        (這樣同時按 J 和 K 時，兩個都會被檢查到)
    --------------------------------------------------------
    """
    # Q3 begin
    if keys[pygame.K_j]:
        car.rotate(-ROTATE_SPEED)
    if keys[pygame.K_k]:
        car.rotate(ROTATE_SPEED)
    # Q3 end
